const ll mod = 1e9 + 7;

void solve() {
	ll n, I;
	cin >> n >> I;
	vl v(n); cin >> v;
	ll k = min(30ll, 8 * I / n);
	ll mx = (1ll << k);
	map<ll, ll> m;
	for (auto x : v) m[x]++;
	vl pre = {0};
	for (auto x : m) pre.pb(pre.back() + x.S);
	ll ans = n, sz = pre.size();
	for (ll l = 1; l < sz; l++) {
		ans = min(ans, n - pre[min(sz - 1, l + mx - 1)] + pre[l - 1]);
	}
	cout << ans << endl;
}

