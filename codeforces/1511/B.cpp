const ll mod = 1e9 + 7;

ll calc(ll x) {
	if (!x) return 1;
	while (true) {
		for (ll i = 2; i * i <= x; i++) {
			if (x % i == 0) {
				x--; goto skip;
			}
		}
		return x;
skip:;
	}
	return x;
}

void solve() {
	ll a, b, c;
	cin >> a >> b >> c;
	a -= c; b -= c;
	ll z = 1;
	while (c-- > 1) z *= 10;
	ll p1 = calc(pow(10, a + 1));
	ll p2 = calc(pow(10, b + 1));
	if (p1 == p2) p2 = calc(p1 - 1);
	cout << p1 * z << " " << p2 * z << endl;
}

int main()
{
	fast;
	int T = 1;
	cin >> T;
	while (T--) {
		solve();
	}
	return 0;
}
