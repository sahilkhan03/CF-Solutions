bool query(ll l, ll r) {
	cout << l << " " << r << endl << flush;
	string s; cin >> s;
	return (s == "Yes");
}

random_device rd;

void solve() {
	ll n, k;
	cin >> n >> k;
	ll lo = 1, hi = n, mid;
	while (lo <= hi) {
		if (hi - lo + 1 > 5 * k) {
			mid = (lo + hi) / 2;
			if (query(lo, mid)) {
				hi = mid;
			} else {
				lo = mid + 1;
			}
		} else {
			uniform_int_distribution<ll> dist(lo, hi);
			ll x = dist(rd);
			if (query(x, x)) return;
		}
		lo = max(1ll, lo - k);
		hi = min(n, hi + k);
	}
}

int main()
{
	fast;
	int T = 1;
	// cin >> T;
	while (T--) {
		solve();
	}
	return 0;
}
