const ll mod = 1e9 + 7;

void solve() {
	ll n; cin >> n;
	vl v(n); cin >> v;
	vl ans(n);
	ll pos = 0, neg = 0;
	vl ps, ng;
	for (int i = 0; i < n; i++) {
		if (v[i] > 0) {
			ans[i] = v[i] / 2;
			if (abs(v[i]) % 2 == 1) ps.pb(i);
			pos += v[i] / 2;
		}
		else  {
			ans[i] = v[i] / 2;
			neg += abs(v[i] / 2);
			if (abs(v[i]) % 2 == 1) ng.pb(i);
		}
	}
	while (pos < neg) {
		ans[ps.back()]++;
		ps.pop_back();
		pos++;
	}
	while (pos > neg) {
		ans[ng.back()]--;
		ng.pop_back();
		neg++;
	}
	for (auto x : ans) cout << x << endl;
}

int main()
{
	fast;
	ll T = 1;
	// cin >> T;
	while (T--) {
		solve();
	}
	return 0;
}
